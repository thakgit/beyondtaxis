export async function generateStaticParams() {
  return Object.keys(blogPosts).map((slug) => ({ slug }))
}

export async function generateMetadata({ params }: { params: { slug: string } }): Promise<Metadata> {
  const post = blogPosts[params.slug]
  if (!post) {
    return { title: 'Post Not Found' }
  }
  return {
    title: `${post.title} - BeyondTAxis.life`,
    description: `Read about ${post.title} on BeyondTAxis.life`,
  }
}

export default function BlogPostPage({ params }: { params: { slug: string } }) {
  const post = blogPosts[params.slug]
  
  if (!post) {
    notFound()
  }

  return (
    <article className="min-h-screen py-20 px-4 sm:px-6 lg:px-8">
      <div className="container mx-auto max-w-4xl">
        <div className="mb-8">
          <div className="flex items-center space-x-3 mb-4">
            <span className="text-xs font-semibold text-primary-400 px-3 py-1 rounded-full bg-primary-500/20">
              {post.category}
            </span>
            <div className="flex items-center space-x-1 text-gray-500 text-sm">
              <Calendar className="w-4 h-4" />
              <span>{format(post.date, 'MMMM d, yyyy')}</span>
            </div>
            <span className="text-gray-500 text-sm">•</span>
            <div className="flex items-center space-x-1 text-gray-500 text-sm">
              <Clock className="w-4 h-4" />
              <span>{post.readTime}</span>
            </div>
          </div>
          
          <h1 className="text-4xl md:text-5xl font-display font-bold mb-6 text-white">
            {post.title}
          </h1>
        </div>

        <div className="glass rounded-xl p-8 prose prose-invert prose-lg max-w-none">
          <ReactMarkdown remarkPlugins={[remarkGfm]}>
            {post.content}
          </ReactMarkdown>
        </div>
      </div>
    </article>
  )
}

